#include "libft.h"

int	ft_counter_number(int n)
{
	int i;

	i = 0;
	if (n < 0)
	{
		i = 1;
		while (n <= -10)
		{
			n = n / -10;
			i++;
		}
	}
	if (n >= 0)
	{
		while (n >= 10)
		{
			n = n / 10;
			i++;
		}
	}
	i = i + 1;
	return (i);
}
