#include "libft.h"

char		*ft_strncpy(char *s1, const char *s2, int n)
{
	char	*s;

	s = s1;
	while (*s2 != 0 && n > 0)
	{
		*s++ = *s2++;
		n--;
	}
	while (n > 0)
	{
		*s++ = '\0';
		n--;
	}
	return (s1);
}
