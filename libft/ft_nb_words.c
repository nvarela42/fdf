#include "libft.h"

int	ft_nb_words(const char *s, char c)
{
	int	i;
	int	nb_word;

	nb_word = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c && s[i] != '\0')
		{
			nb_word++;
			while (s[i] != c && s[i] != '\0')
				i++;
			i--;
		}
		i++;
	}
	return (nb_word);
}
